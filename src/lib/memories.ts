export type Memory = NonNullable<ReturnType<typeof getMemoryById>>;

const getMutuallyExclusiveMemories = ({
  name,
  traveler,
  travelerMemoryLocation,
}: {
  name: string;
  traveler: string;
  travelerMemoryLocation: string;
}) =>
  Object.entries(memories)
    .filter(
      ([, memory]) =>
        memory.name !== name &&
        memory.traveler &&
        memory.traveler === traveler &&
        memory.travelerMemoryLocation === travelerMemoryLocation,
    )
    .map(([, { name }]) => name);

export const getMemories = () =>
  Object.keys(memories)
    .map((id) => getMemoryById(id))
    .filter(Boolean)
    .toSorted(
      (a, b) =>
        compareRarities(a.rarity, b.rarity) ||
        a.traveler.localeCompare(b.traveler) ||
        a.name.localeCompare(b.name),
    );

export const getMemoryById = (id: string) => {
  if (id in memories) {
    const memoryId = id as keyof typeof memories;
    const memory = memories[memoryId];
    return {
      id: memoryId,
      mutuallyExclusive: getMutuallyExclusiveMemories(memory),
      ...memory,
    } as const;
  }
};

export const getMemoryRarities = () =>
  [...new Set(Object.values(memories).map(({ rarity }) => rarity))].toSorted(
    compareRarities,
  );

export const getMemoryTypes = () => [
  ...new Set(Object.values(memories).map(({ type }) => type)),
];

export const getMemoryTags = () =>
  [...new Set(Object.values(memories).flatMap(({ tags }) => tags))].toSorted();
